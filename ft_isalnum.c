#include "libft.h"

int	ft_isalnum(int smb)
{
	return (((smb >= '0' && smb <= '9') || (smb >= 'a' && smb <= 'z')
	|| (smb >= 'A' && smb <= 'Z')) ? 8 : 0);
}
